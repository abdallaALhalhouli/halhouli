name = "Abdallah"
print("hallo this is "+ name.lower() +  " and this is my first python code")
#name.lower() is used to make all the letters in the name small
print("hallo this is "+ name.upper() +  " and this is my first python code")
#name.upper() is used to make all the letters in the name capital   
#name.isupper() is used to check if all the letters in the name are capital or not and it will return true or false
print(name.isupper())
#name.islower() is used to check if all the letters in the name are small or not and it will return true or false
print(name.islower())
print(name.upper().islower())
print(name.lower().isupper())